import UIKit

extension UIColor {
    
    class func blackStone() -> UIColor {
        return UIColor(colorLiteralRed: 0.15, green: 0.15, blue: 0.15, alpha: 1.0)
    }
    
    class func whiteStone() -> UIColor {
        return UIColor(colorLiteralRed: 0.85, green: 0.85, blue: 0.85, alpha: 1.0)
    }
    
}
